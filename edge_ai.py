async def generate():
    for lang, texts in SENTENCES.items():
        voice = VOICE_MAP[lang]
        for i, text in enumerate(texts, start=1):
            rate = random.choice(["-10%", "+0%", "+10%"])
            pitch = random.choice(["-5Hz", "+0Hz", "+5Hz"])
            volume = random.choice(["-5%", "+0%", "+5%"])

            filename = f"dataset/ai_edge/{lang}_edge_{i}.mp3"

            communicate = edge_tts.Communicate(
                text=text,
                voice=voice,
                rate=rate,
                pitch=pitch,
                volume=volume
            )

            await communicate.save(filename)
            print("Saved:", filename)

asyncio.run(generate())
print("✅ Edge TTS dataset generation complete!")
